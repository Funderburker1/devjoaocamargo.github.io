                papéis de cada jogador em relação à sua equipe para atingir os objetivos e conquistar território, que
                podem ser: topo (top laner), atirador (ou ADC, ou ad carry), suporte, meio (mid laner) e selva
                (jungler).
            </p>
            <p class="text-white m-0 text-medium padding-20px-top justify-text">
                São mais de 140 campeões disponíveis para representar o jogador, que deve, ao longo da partida,
